public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World"); // Chương trình in ra màn hình chuỗi "Hello World"
    }
}

// Trong lớp HelloWorld, chúng ta có một phương thức (method) main().
// Phương thức này in ra màn hình chuỗi "Hello World".
// Đây là một chương trình dòng lệnh (command-line program), tức là nó chạy trên
// shell hoặc cửa sổ DOS và in output ra đó.